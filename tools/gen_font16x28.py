#!/usr/bin/env python3
"""
Generate font16x28.h — a custom 16x28 bitmap font for SSD1306 (page-oriented).

Each glyph is defined as a 16x28 ASCII-art grid (the visible area), then
padded to 16x32 with 2px top and 2px bottom padding before conversion to
SSD1306 page format: 4 pages of 16 bytes each (column-major within page,
LSB = top pixel within page).

The 2px top/bottom padding leaves room for the half-degree dot indicator
above the digits and for the square frame of the long-press overlay.

Glyphs covered: 0-9, space, dash, H, I, L, O

Usage:
    python3 gen_font16x28.py > ../main/font16x28.h
"""

import sys

W = 16
H_VISIBLE = 28  # visible glyph height
H_TOTAL = 32    # total height including padding
PAD_TOP = 2     # empty rows above glyph
PAD_BOT = 2     # empty rows below glyph

def parse_grid(art):
    """Parse a 28-line ASCII-art string into a 32x16 grid with 2px top/bottom padding.
    '#' or 'X' = pixel on, anything else = off."""
    lines = art.strip('\n').split('\n')
    assert len(lines) == H_VISIBLE, f"Expected {H_VISIBLE} lines, got {len(lines)}"
    grid = []
    # Top padding
    for _ in range(PAD_TOP):
        grid.append([0] * W)
    # Visible area
    for line in lines:
        row = []
        for i in range(W):
            ch = line[i] if i < len(line) else '.'
            row.append(1 if ch in ('#', 'X') else 0)
        grid.append(row)
    # Bottom padding
    for _ in range(PAD_BOT):
        grid.append([0] * W)
    return grid

def grid_to_pages(grid):
    """Convert 16x32 grid to 4 pages of 16 bytes each (SSD1306 format)."""
    pages = []
    for page in range(4):
        page_data = []
        for col in range(W):
            byte = 0
            for bit in range(8):
                y = page * 8 + bit
                if grid[y][col]:
                    byte |= (1 << bit)
            page_data.append(byte)
        pages.append(page_data)
    return pages

def grid_to_art(grid):
    """Convert grid back to ASCII art for comments (full 32 rows including padding)."""
    lines = []
    for y in range(H_TOTAL):
        lines.append(''.join('#' if grid[y][x] else '.' for x in range(W)))
    return lines


